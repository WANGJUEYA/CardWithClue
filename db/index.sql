DROP TABLE IF EXISTS DATA_CLUE;
CREATE TABLE DATA_CLUE
(
    ID         INTEGER PRIMARY KEY AUTOINCREMENT,
    SOURCE     INTEGER NOT NULL,
    TARGET     INTEGER NOT NULL,
    CLUE_GROUP TEXT    NOT NULL,
    CLUE_TIME  DATETIME,
    CLUE_KEY   TEXT    NOT NULL,
    CLUE_LINK  TEXT,
    CLUE_DESC  TEXT,
    CLUE_JSON  TEXT
);
