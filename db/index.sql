DROP TABLE IF EXISTS DATA_CLUE;
CREATE TABLE DATA_CLUE
(
    ID          INTEGER PRIMARY KEY AUTOINCREMENT,
    CLUE_SOURCE INTEGER NOT NULL,
    CLUE_TARGET INTEGER NOT NULL,
    CLUE_GROUP  TEXT    NOT NULL,
    CLUE_TIME   DATETIME,
    CLUE_KEY    TEXT    NOT NULL,
    CLUE_LINK   TEXT,
    CLUE_DESC   TEXT
);
CREATE INDEX INDEX_DATA_CLUE_GROUP ON DATA_CLUE (CLUE_TIME);