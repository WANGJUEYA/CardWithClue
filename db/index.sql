DROP TABLE IF EXISTS DATA_CARD;
CREATE TABLE DATA_CARD
(
    ID         INTEGER PRIMARY KEY AUTOINCREMENT,
    CARD_GROUP TEXT NOT NULL,
    CARD_KEY   TEXT NOT NULL,
    CARD_LINK  TEXT,
    CARD_DESC  TEXT
);

CREATE INDEX INDEX_DATA_CARD_GROUP ON DATA_CARD (CARD_GROUP);

